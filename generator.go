func generate(filename, outputFilename, manifest string) {
	// Get file
	fileAbs, err := filepath.Abs(filename)
	if err != nil {
		panic(err)
	}

	content, err := os.ReadFile(fileAbs)
	if err != nil {
		panic(err)
	}
	contentStr := "`" + string(content) + "`"

	// Find the output.
	addonController, err := os.Create(outputFilename + ".go")
	if err != nil {
		panic(err)
	}
	defer addonController.Close()

	// Store file contents.
	type Info struct {
		YAML        string
		File        string
		ExportedVar string
	}
	mi := Info{
		YAML:        contentStr,
		File:        filename,
		ExportedVar: manifest,
	}

	// Generate template.
	manifesTemplate := template.Must(template.New("deployment-shard").Parse(deploymentShardTemplate))
	if err := manifesTemplate.Execute(addonController, mi); err != nil {
		panic(err)
	}
}

